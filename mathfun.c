#include<stdio.h>
#include<math.h>
void main()
{
	printf("\n ceil value  :  %.2f ",ceil(11.2));
	printf("\n ceil value  :  %.2f",ceil(11.8));
	
	printf("\n floor value : %.2f",floor(11.2));
	printf("\n floor value : %.2f",floor(11.8));
	
	printf("\n sqrt value  : %.2f",sqrt(16));
	printf("\n sqrt value  : %.2f",sqrt(7));
	
	printf("\n power value : %.2f",pow(2,4));
	printf("\n power value : %.2f",pow(2.2,3));
	
	printf("\n absolute value : %d",abs(12));
	printf("\n absolute value : %.2f",abs(-11.3));
	
	getch();
}
